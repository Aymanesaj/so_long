#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	c;

	i = 0;
	sign = 1;
	c = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == 45 || str[i] == 43)
	{
		if (str[i] == 45)
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		c = c * 10 + (str[i] - '0');
		i++;
	}
	return (c * sign);
}
